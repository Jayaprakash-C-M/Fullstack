console.log("printing stars pattern 1 to 5")
y=1
while(y<=5)
{
    
    if(y==1)
    {
        console.log("*")
        
    }
    else if(y==2)
    {
        console.log("**")
        
    }
    else if(y==3)
    {
        console.log("***")
       
    }
    else if(y==4)
    {
        console.log("****")
        
    }
    else if(y==5)
    {
        console.log("*****")
    }
    
y++
}

